 private void populatePartyNames() {
        try (Connection conn = DatabaseConnection.connect()) {
            if (conn != null) {
                String sql = "SELECT nomp FROM candidate";
                try (PreparedStatement preparedStatement = conn.prepareStatement(sql);
                     ResultSet resultSet = preparedStatement.executeQuery()) {

                    while (resultSet.next()) {
                        String nomp = resultSet.getString("nomp");
                        jComboBox1.addItem(nomp);
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace(); // Ou lancez une nouvelle exception si nécessaire
        }
    }
 
private void displayPartyPhoto(String partyName) {
    try (Connection conn = DatabaseConnection.connect()) {
        if (conn != null) {
            String sql = "SELECT partysign FROM candidate WHERE nomp = ?";
            try (PreparedStatement preparedStatement = conn.prepareStatement(sql)) {
                preparedStatement.setString(1, partyName);

                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    if (resultSet.next()) {
                        // Récupérer les données binaires de l'image depuis la base de données
                        byte[] imageData = resultSet.getBytes("partysign");

                        // Convertir les données binaires en ImageIcon
                        ImageIcon partyImage = new ImageIcon(imageData);

                        // Définir la taille fixe pour le composant JLabel
                        int labelWidth = 200; // Remplacez par la taille souhaitée en pixels
                        int labelHeight = 200; // Remplacez par la taille souhaitée en pixels

                        // Redimensionner l'image sans déformer
                        Image scaledImage = partyImage.getImage().getScaledInstance(labelWidth, labelHeight, Image.SCALE_SMOOTH);

                        // Créer une nouvelle ImageIcon à partir de l'image redimensionnée
                        ImageIcon scaledPartyImage = new ImageIcon(scaledImage);

                        // Appliquer l'image redimensionnée au jLabel5 (au lieu de jLabel1)
                        jLabel5.setIcon(scaledPartyImage);

                        // Centrer l'image à l'intérieur de jLabel5
                        int xOffset = (labelWidth - scaledPartyImage.getIconWidth()) / 2;
                        int yOffset = (labelHeight - scaledPartyImage.getIconHeight()) / 2;

                        jLabel5.setIcon(new ImageIcon(scaledImage));
                        jLabel5.setBounds(xOffset, yOffset, scaledPartyImage.getIconWidth(), scaledPartyImage.getIconHeight());
                    }
                }
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}



