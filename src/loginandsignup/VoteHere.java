    private void vote(String partyName, String password) {
        if (checkPasswordMatch(password)) {
            updateVoteInDatabase(partyName, password);
            JOptionPane.showMessageDialog(this, "Vote enregistré !");
        } else {
            JOptionPane.showMessageDialog(this, "Mot de passe incorrect !");
        }
    }

    private boolean checkPasswordMatch(String enteredPassword) {
        try (Connection conn = DatabaseConnection.connect()) {
            if (conn != null) {
                String sql = "SELECT * FROM users WHERE password = ?";
                try (PreparedStatement preparedStatement = conn.prepareStatement(sql)) {
                    preparedStatement.setString(1, enteredPassword);

                    try (ResultSet resultSet = preparedStatement.executeQuery()) {
                        return resultSet.next(); // Vérifie si le mot de passe existe dans la base de données
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }
 
   
