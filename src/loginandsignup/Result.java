    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try (Connection conn = DatabaseConnection.connect()) {
            if (conn != null) {
                // Étape 1: Récupérer les Party Names de la table "candidate"
                Map<String, Integer> partyVotes = new HashMap<>();

                String partyNamesQuery = "SELECT nomp FROM candidate";
                try (PreparedStatement partyNamesStatement = conn.prepareStatement(partyNamesQuery);
                    ResultSet partyNamesResultSet = partyNamesStatement.executeQuery()) {

                    while (partyNamesResultSet.next()) {
                        String partyName = partyNamesResultSet.getString("nomp");
                        partyVotes.put(partyName, 0); // Initialiser le nombre de votes à 0
                    }
                }

                // Étape 2: Calculer le nombre de votes pour chaque Party Name à partir de la table "users"
                String votesQuery = "SELECT vote FROM users";
                try (PreparedStatement votesStatement = conn.prepareStatement(votesQuery);
                    ResultSet votesResultSet = votesStatement.executeQuery()) {

                    while (votesResultSet.next()) {
                        String votedParty = votesResultSet.getString("vote");
                        if (partyVotes.containsKey(votedParty)) {
                            // Incrémenter le nombre de votes pour le parti correspondant
                            partyVotes.put(votedParty, partyVotes.get(votedParty) + 1);
                        }
                    }
                }

                // Étape 3: Trouver le parti avec le plus de votes
                String mostVotedParty = null;
                int maxVotes = 0;

                for (Map.Entry<String, Integer> entry : partyVotes.entrySet()) {
                    if (entry.getValue() > maxVotes) {
                        mostVotedParty = entry.getKey();
                        maxVotes = entry.getValue();
                    }
                }

                // Étape 4: Créer une structure de données pour le JTable
                Vector<Vector<String>> data = new Vector<>();
                Vector<String> row = new Vector<>();
                row.add(mostVotedParty); // Party Name
                row.add(String.valueOf(maxVotes)); // Number of Votes
                data.add(row);

                // Étape 5: Créer le modèle de tableau et le définir pour le JTable
                Vector<String> columnNames = new Vector<>();
                columnNames.add("Party Name");
                columnNames.add("Number of Votes");

                DefaultTableModel model = new DefaultTableModel(data, columnNames);
                jTable1.setModel(model);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

